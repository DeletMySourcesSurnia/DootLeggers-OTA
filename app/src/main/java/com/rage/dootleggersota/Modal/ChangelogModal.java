package com.rage.dootleggersota.Modal;

public class ChangelogModal {

    private String title, description;

    public ChangelogModal(String title, String description) {
        this.title = title;
        this.description = description;
    }

    public String getTitle() {
        return title;
    }

    public String getDescription() {
        return description;
    }

}
